const apiKey = 'xxxxxxxxxxxxxxxxxxxxxxxxx';
const weatherDataURL = 'http://api.openweathermap.org/data/2.5/weather';

class WeatherModel {
  Future<dynamic> getCityWeather(String cityName) async {
    NetworkHelper networkHelper = NetworkHelper(
        '$weatherDataURL?q=${cityName}&appid=$apiKey&units=metric');
    return await networkHelper.getData();
  }

  Future<dynamic> getLocationWeather() async {
    Location loc = Location();

    await loc.getCurrentLocation();

    NetworkHelper networkHelper = NetworkHelper(
        '$weatherDataURL?lat=${loc.latitude}&lon=${loc.longitude}&appid=$apiKey&units=metric');
    return await networkHelper.getData();
  }

